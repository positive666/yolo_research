    def forward(self, x, augment=False, profile=False, visualize=False, kp_flip=None,
                scales=[0.5, 1, 2], flips=[None, 3, None]):
        if augment:
            return self.forward_augment(x, kp_flip, s=scales, f=flips)  # augmented inference, None
